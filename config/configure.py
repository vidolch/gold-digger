def check_dependencies():
    """Check if required packages are installed."""
    required_packages = [
        ('yfinance', 'yfinance'),
        ('pandas', 'pandas'),
        ('ollama', 'ollama'),
        ('dotenv', 'python-dotenv'),
        ('matplotlib', 'matplotlib')
    ]

    missing = []
    for package, pip_name in required_packages:
        try:
            __import__(package)
        except ImportError:
            missing.append(pip_name)

    return missing

def install_dependencies(missing_packages):
    """Install missing dependencies."""
    print(f"ğŸ“¦ Installing missing packages: {', '.join(missing_packages)}")
    try:
        subprocess.check_call([
            sys.executable, '-m', 'pip', 'install', '-r', 'requirements.txt'
        ])
        print("âœ… All dependencies installed successfully!")
        return True
    except subprocess.CalledProcessError as e:
        print(f"âŒ Failed to install dependencies: {e}")
        return False

def check_ollama():
    """Check Ollama installation and model availability."""
    print("\nğŸ” Checking Ollama setup...")

    # Check if Ollama is installed
    try:
        result = subprocess.run(['ollama', '--version'],
                              capture_output=True, text=True)
        if result.returncode == 0:
            print("âœ… Ollama is installed")
        else:
            print("âŒ Ollama is not installed")
            return False
    except FileNotFoundError:
        print("âŒ Ollama command not found. Please install from https://ollama.ai")
        return False

    # Check if Ollama service is running
    try:
        import ollama
        client = ollama.Client()
        models = client.list()
        print("âœ… Ollama service is running")

        # Check for gpt-oss:20b model
        model_names = [model['model'] for model in models['models']]
        if 'gpt-oss:20b' in model_names:
            print("âœ… gpt-oss:20b model is available")
            return True
        else:
            print("âš ï¸  gpt-oss:20b model not found")
            print("Available models:", ', '.join(model_names) if model_names else "None")

            response = input("Install gpt-oss:20b model now? (y/N): ")
            if response.lower() == 'y':
                return install_ollama_model()
            return False

    except Exception as e:
        print(f"âŒ Cannot connect to Ollama service: {e}")
        print("Make sure Ollama is running with: ollama serve")
        return False

def install_ollama_model():
    """Install the required Ollama model."""
    print("ğŸ“¥ Installing gpt-oss:20b model (this may take a while)...")
    try:
        subprocess.check_call(['ollama', 'pull', 'gpt-oss:20b'])
        print("âœ… gpt-oss:20b model installed successfully!")
        return True
    except subprocess.CalledProcessError as e:
        print(f"âŒ Failed to install model: {e}")
