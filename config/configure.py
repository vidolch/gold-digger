def main():
    """Main configuration function."""
    parser = argparse.ArgumentParser(description='Gold Digger Configuration Utility')
    parser.add_argument('--quick', '-q', action='store_true',
                       help='Quick setup with defaults')
    parser.add_argument('--install-deps', action='store_true',
                       help='Install dependencies only')
    parser.add_argument('--install-model', action='store_true',
                       help='Install Ollama model only')
    parser.add_argument('--test', action='store_true',
                       help='Test current setup')
    parser.add_argument('--reset', action='store_true',
                       help='Reset configuration to defaults')

    args = parser.parse_args()

    print_banner()

    if args.install_deps:
        missing = check_dependencies()
        if missing:
            install_dependencies(missing)
        else:
            print("âœ… All dependencies already installed")
        return

    if args.install_model:
        install_ollama_model()
        return

    if args.test:
        success = test_setup()
        sys.exit(0 if success else 1)

    if args.reset:
        if Path('.env').exists():
            Path('.env').unlink()
            print("âœ… Configuration reset (removed .env file)")
        else:
            print("â„¹ï¸  No configuration file to reset")
        return

    # Full setup process
    print("ğŸš€ Starting Gold Digger setup process...")

    # Step 1: Check dependencies
    print("\nğŸ“¦ Checking Python dependencies...")
    missing = check_dependencies()
    if missing:
        print(f"Missing packages: {', '.join(missing)}")
        if input("Install missing packages? (Y/n): ").strip().lower() != 'n':
            if not install_dependencies(missing):
                print("âŒ Setup failed at dependency installation")
                sys.exit(1)
    else:
        print("âœ… All Python dependencies are installed")

    # Step 2: Check Ollama
    if not check_ollama():
        if input("Continue without Ollama setup? (y/N): ").strip().lower() != 'y':
            print("âŒ Setup aborted - Ollama required for AI analysis")
            sys.exit(1)
        print("âš ï¸  Continuing without AI analysis capabilities")

    # Step 3: Create configuration
    if args.quick:
        # Quick setup with defaults
        if not Path('.env').exists():
            Path('.env.example').rename('.env')
            print("âœ… Quick setup complete - using default configuration")
        else:
            print("â„¹ï¸  Configuration file already exists")
    else:
        # Interactive setup
        create_env_file()

    # Step 4: Test setup
    print(f"\nğŸ§ª Testing setup...")
    if test_setup():
        show_next_steps()
        print("\nğŸ‰ Gold Digger is ready to use!")
    else:
        print("\nâŒ Setup incomplete - please check errors above")
        sys.exit(1)

if __name__ == "__main__":
    main()
